    function startBattle() {
        if (gameState.currentStage > gameState.maxStage) {
            showNotification('已通关所有关卡！');
            return;
        }
        
        var enemyIndex = Math.min(gameState.currentStage - 1, gameData.enemies.length - 1);
        battleState.enemy = gameData.enemies[enemyIndex];
        battleState.playerHp = gameState.stats.hp;
        battleState.enemyHp = battleState.enemy.hp;
        battleState.isBattle = true;
        battleState.turn = 1;
        
        $('battle-stage').textContent = gameState.currentStage;
        $('battle-enemy-name').textContent = battleState.enemy.name;
        updateBattleUI();
        
        $('game-interface').style.display = 'none';
        $('battle-interface').style.display = 'flex';
        
        setTimeout(function() { performBattleTurn(); }, 1000);
    }
    
    function performBattleTurn() {
        if (!battleState.isBattle) return;
        
        var playerAttacksFirst = gameState.stats.speed >= battleState.enemy.speed;
        
        if (playerAttacksFirst) {
            playerAttack();
            if (battleState.enemyHp <= 0) {
                endBattle(true);
                return;
            }
            setTimeout(function() { enemyAttack(); }, 1000);
        } else {
            enemyAttack();
            if (battleState.playerHp <= 0) {
                endBattle(false);
                return;
            }
            setTimeout(function() { playerAttack(); }, 1000);
        }
        
        if (battleState.playerHp > 0 && battleState.enemyHp > 0) {
            battleState.turn++;
            setTimeout(function() { performBattleTurn(); }, 1500);
        }
    }
    
    function playerAttack() {
        var damage = gameState.stats.attack - battleState.enemy.defense * 0.5;
        damage = Math.max(damage, 1);
        
        battleState.enemyHp -= damage;
        battleState.enemyHp = Math.max(battleState.enemyHp, 0);
        
        addBattleLog('玩家战机造成 ' + Math.floor(damage) + ' 点伤害！');
        updateBattleUI();
    }
    
    function enemyAttack() {
        var damage = battleState.enemy.attack - gameState.stats.defense * 0.5;
        damage = Math.max(damage, 1);
        
        battleState.playerHp -= damage;
        battleState.playerHp = Math.max(battleState.playerHp, 0);
        
        addBattleLog(battleState.enemy.name + ' 造成 ' + Math.floor(damage) + ' 点伤害！');
        updateBattleUI();
    }
    
    function updateBattleUI() {
        var playerPercent = (battleState.playerHp / gameState.stats.maxHp) * 100;
        var enemyPercent = (battleState.enemyHp / battleState.enemy.hp) * 100;
        
        $('player-hp-bar').style.width = playerPercent + '%';
        $('player-hp-text').textContent = Math.floor(battleState.playerHp) + '/' + gameState.stats.maxHp;
        
        $('enemy-hp-bar').style.width = enemyPercent + '%';
        $('enemy-hp-text').textContent = Math.floor(battleState.enemyHp) + '/' + battleState.enemy.hp;
    }
    

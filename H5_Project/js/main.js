    function checkAchievements(context) {
        if (!context) return;
        
        if (context.type === 'coreUpgrade' && context.level >= 3) {
            if (!gameState.achievementProgress.coreThreeStar) {
                gameState.achievementProgress.coreThreeStar = true;
                unlockAchievement('ach_final_weapon');
            }
        }
        
        if (context.type === 'battleWin') {
            if (context.enemy === 'spark' && context.usedSkill === '赛道魔术') {
                if (!gameState.achievementProgress.defeatedSpark) {
                    gameState.achievementProgress.defeatedSpark = true;
                    unlockAchievement('ach_spark_song');
                }
            }
            
            if (context.noDamage) {
                if (!gameState.achievementProgress.noDamageWin) {
                    gameState.achievementProgress.noDamageWin = true;
                    unlockAchievement('ach_ace_combat');
                }
            }
            
            if (context.currentAircraft) {
                if (gameState.achievementProgress.wonAircraftList.indexOf(context.currentAircraft) === -1) {
                    gameState.achievementProgress.wonAircraftList.push(context.currentAircraft);
                    if (gameState.achievementProgress.wonAircraftList.length >= gameData.aircraft.length) {
                        unlockAchievement('ach_all_models');
                    }
                }
            }
        }
        
        if (context.type === 'equipmentCheck') {
            var threeStarCount = 0;
            gameState.equippedItems.forEach(function(item) {
                if (item && item.level >= 3) threeStarCount++;
            });
            if (threeStarCount >= 7) {
                if (!gameState.achievementProgress.sevenThreeStar) {
                    gameState.achievementProgress.sevenThreeStar = true;
                    unlockAchievement('ach_graduation');
                }
            }
        }
        
        if (gameState.unlockedAircraft.length >= gameData.aircraft.length) {
            if (!gameState.achievementProgress.allAircraftUnlocked) {
                gameState.achievementProgress.allAircraftUnlocked = true;
                unlockAchievement('ach_blue_sky');
            }
        }
        
        saveSystem.save();
    }
    

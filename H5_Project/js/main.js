    function buyItem(itemId) {
        var item = findItemById(itemId);
        if (!item) return;
        
        if (gameState.gold < item.price) {
            showNotification('金币不足！');
            return;
        }
        
        gameState.gold -= item.price;
        
        var emptySlot = gameState.equippedItems.indexOf(null);
        if (emptySlot !== -1) {
            gameState.equippedItems[emptySlot] = item;
            applyItemStats(item, true);
            showNotification('装备已装备到槽位 ' + (emptySlot + 1));
        } else {
            gameState.inventory.push(item);
            showNotification('装备已放入背包');
        }
        
        updateUI();
    }
    
    function findItemById(id) {
        for (var type in gameData.equipment) {
            var item = gameData.equipment[type].find(function(i) { return i.id === id; });
            if (item) return item;
        }
        return null;
    }
    
    function applyItemStats(item, add) {
        var multiplier = add ? 1 : -1;
        if (item.stats) {
            for (var stat in item.stats) {
                if (gameState.stats[stat] !== undefined) {
                    gameState.stats[stat] += item.stats[stat] * multiplier;
                }
            }
        }
        if (item.damage && item.damage > 0) {
            gameState.stats.attack += Math.floor(item.damage / 5) * multiplier;
        }
    }
    
